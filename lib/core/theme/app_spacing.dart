/// Spacing and corner-radius tokens for the Brutl UI system.
final class AppSpacing {
  const AppSpacing._();

  /// Tiny spacing for icon-text gaps.
  static const double xs = 4.0;

  /// Small related-element spacing.
  static const double sm = 8.0;

  /// Tight inner padding baseline.
  static const double md = 12.0;

  /// Standard element spacing.
  static const double lg = 16.0;

  /// Minimum horizontal page padding.
  static const double xl = 20.0;

  /// Major section spacing.
  static const double xxl = 24.0;

  /// Large section break spacing.
  static const double xxxl = 32.0;

  /// Chips, tags, and badges.
  static const double borderRadiusSmall = 8.0;

  /// Inputs and compact cards.
  static const double borderRadiusMedium = 12.0;

  /// Standard cards.
  static const double borderRadiusLarge = 16.0;

  /// Hero cards.
  static const double borderRadiusXL = 20.0;

  /// Sheets and modal containers.
  static const double borderRadiusXXL = 28.0;

  /// Pills and circular elements.
  static const double borderRadiusFull = 100.0;
}
